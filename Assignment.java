public class Assignment {
    int id;
    String courseName;
    String title;
    String dueDate;
    int priority;
    boolean completed;

    public Assignment(int id, String courseName, String title, String dueDate, int priority, boolean completed){
        this.id = id;
        this.courseName = courseName;
        this.title = title;
        this.dueDate = dueDate;
        this.priority = priority;
        this.completed = completed;

    }
    
}
